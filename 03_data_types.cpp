#include <iostream>
using namespace std;

int main() {

    // All int typees
    int a = 33;
    short b = 4;
    long c = 678;
    long long d = 5678;
    cout << "\nAll Ints\n" << a << b << c << d;

    // All float types
    float e = 3.4;
    double f = 34.456;
    long double g = 456.789; 
    cout << "\nAll Floats\n" << e << f << g;

    // Boolean types
    bool h = true;
    cout << "\nBooleans\n" << h;
    
    // String and characters types
    // Character should be in single inverted quote
    char i = 't';
    string j = "Hello world";
    cout << "\nStrings and characters\n" << i << j;
    
    return 0;
}

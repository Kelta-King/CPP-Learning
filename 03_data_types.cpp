#include <iostream>
using namespace std;

int main() {

    // All int typees
    int a = 33;
    short b = 4;
    long c = 678;
    long long d = 5678;
    cout << "\n\nAll Ints\n" << a << "\n" << b << "\n" << c << "\n" << d;

    // All float types
    float e = 3.4;
    double f = 34.456;
    long double g = 456.789; 
    cout << "\n\nAll Floats\n" << e << "\n" << f << "\n" << g;

    // Boolean types
    bool h = true;
    cout << "\n\nBooleans\n" << h;
    
    // String and characters types
    // Character should be in single inverted quote
    char i = 't';
    string j = "Hello world";
    cout << "\n\nStrings and characters\n" << i << "\n" << j;
    
    return 0;
}
